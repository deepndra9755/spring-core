package com.spring.SetDemo;

import java.util.Date;
import java.util.Set;

public class SetInsertionDemo {
	
Set<Integer>numbers;
Set<Date>dops;
public SetInsertionDemo(Set<Integer> numbers, Set<Date> dops) {
	
	this.numbers = numbers;
	this.dops = dops;
}
@Override
public String toString() {
	return "SetInsertionDemo [numbers=" + numbers + ", dops=" + dops + "]";
}





}

