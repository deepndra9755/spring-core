package com.spring.ArrayDemo;

import java.util.Date;
import java.util.List;

public class ListInsertionDemo {
	List<String> name;
	List<Date> datess;
	public void setName(List<String> name) {
		this.name = name;
	}
	public void setDatess(List<Date> datess) {
		this.datess = datess;
	}
	@Override
	public String toString() {
		return "ListInsertionDemo [name=" + name + ", datess=" + datess + "]";
	}
	

}
