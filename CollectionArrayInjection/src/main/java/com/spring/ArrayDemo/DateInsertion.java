package com.spring.ArrayDemo;

import java.util.Arrays;
import java.util.Date;

public class DateInsertion {
	Date dt[];

	public void setDt(Date[] dt) {
		this.dt = dt;
	}

	@Override
	public String toString() {
		return "DateInsertion [dt=" + Arrays.toString(dt) + "]";
	}
	
	

}
